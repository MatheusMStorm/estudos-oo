package roteiro4.parte2;

public class Aluno {

    public int matricula;
    public String nome;
    public String curso;
    public int anoIngresso;

    Aluno(String pNome, int pMatricula, String pCurso, int pAnoIngresso) {
        nome = pNome;
        matricula = pMatricula;
        curso = pCurso;
        anoIngresso = pAnoIngresso;
    }
}
